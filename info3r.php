<p style="font:Verdana, Geneva, sans-serif"><h2>PENGURANGAN</h2></p>
<p style="float:left"><img src="image/tag-recycle.png"></p>

<p><b>DEFINISI</b></p>
<p align="justify">Pengurangan bermaksud mengurangkan dan mengelakkan daripada membeli barang yang tidak diperlukan. Ianya juga bermaksud pencegahan/pengurangan sisa atau pengurangan sumber dimana penggunaan dan pembuangan barang-barang dikurangkan. Ini termasuklah :
	<ul>
		<li>Membeli barang yang tahan lama dan tahan lasak</li>
        <li>Membeli barang-barang yang dibungkus dari bahan bebas toksik</li>
        <li>Mereka bentuk produk supaya mengurangkan penggunaan bahan mentah, mempunyai jangka hayat yang  panjang atau boleh diguna pakai semula</li>
    </ul>
</p>

<p><b>MENGAPA KITA PERLU MENGURANGKAN SISA?</b></p>
<p align="justify">Pengurangan sisa sebenarnya adalah satu cara untuk mengelakkan penghasilan sisa, jadi ianya merupakan kaedah yang sesuai dalam pengurusan sisa serta dapat memelihara alam sekitar.</p>

<p><b>CARA-CARA UNTUK MENGURANGKAN SISA</b></p>

<p align="justify">
<ul type="circle">
	<li>Mengurangkan penggunaan plastik. 1/3 daripada komposisi sisa ialah bahan plastik pembungkus.
Beli barangan dengan pembungkus yang boleh di kitar semula atau yang diperbuat daripada bahan-bahan kitar semula.
Penggunaan beg plastik tidak digalakkan jika anda membeli barang dalam kuantiti yang sedikit.</li>
	<li>Apabila anda membuat sedikit perubahan pada senarai pembelian, secara tidak langsung anda mengurangkan jumlah sisa yang dihasilkan dari rumah atau sekitarnya.</li>
    <li>Anda boleh mengurangkan jumlah sisa yang terhasil dengan memilih jenis sisa yang dibuang. Ini adalah satu proses yang senang dan mudah dimana anda hanya perlu mengikuti panduan mudah seperti di bawah bagi mengurangkan sisa anda di rumah, sekolah ataupun di tempat kerja.</li>
</ul>
</p>

<p align="justify"><b>Beli apa yang anda perlu</b>
Elakkan pembelian barang yang tidak diperlukan untuk mengurangkan penghasilan sisa. Barang-barang yang jarang digunakan boleh dipinjam atau dikongsi dengan orang lain.</p>

<p align="justify"><b>Beli barang yang boleh digunakan semula</b>
Beli produk berasaskan botol kaca atau tin dan bateri yang boleh dicas semula. Barang — barang seperti ini boleh digunakan berulang kali, seterusnya mengurangkan penghasilan sisa.</p>
<p align="justify"><center><img src="image/girl-recycling-plastic-bottles.png" width="300px"></center></p>
<p align="justify"><b>Beli pembersih rumah serba guna </b>
Ianya lebih menjimatkan dan menguntungkan daripada membeli banyak jenis pembersih rumah.</p>

<p align="justify"><b>Beli barangan dalam bungkusan yang sedikit</b>
Ia dapat mengurangkan pembuangan bungkusan dalam tong sampah anda. Untuk barang yang selalu digunakan, beli dalam kuantiti yang banyak kerana ianya dapat menjimatkan wang anda serta mengurangkan pembungkusan.</p>

<p align="justify"><b>Jual atau derma barang — barang yang tidak diperlukan</b>
Menderma/memberi barang-barang yang tidak perlu kepada keluarga, kawan atau jiran dapat mengurangkan sampah. Anda juga boleh menjual barang kepunyaan anda melalui jualan car-boot untuk memperoleh wang tambahan.</p>

<p align="justify"><b>Mengurangkan penggunaan kertas dengan membatalkan surat yang tidak diperlukan</b> Kita dapat mengurangkan penggunaan kertas dengan membayar bil secara on-line.
<p align="justify" style="float:left"><img src="image/Recycle bin full.png" width="200px"></p>
<p align="justify"><b>Mengurangkan pembelian produk yang mengandungi bahan-bahan merbahaya</b> Kebanyakan barang-barang berbahaya seperti racun perosak tidak boleh dikitar semula kerana mengandungi bahan kimia. Kurangkan membeli barang-barang berbahaya seperti ini.

<p align="justify"><b>Sebolehnya beli barang yang tidak bertoksik</b>
Kebanyakan barang yang bertoksik seperti minyak motor dan racun pemusnah serangga tidak boleh digunakan semula.

<p align="justify"><b>Cari kaedah selamat bagi barangan rumah yang merbahaya</b>
'Baking soda' dan cuka boleh digunakan sebagai bahan pencuci di rumah. Minyak zaitun dan jus limau adalah alternatif bagi penggilap perabot kimia dan penggunaan lilin wangi sebagai ganti kepada alat penyegar udara kimia.
</p>
